(
		CATEGORY_ID    VARCHAR2 (10),
		AR_GLCODE      VARCHAR2 (30),
		CATEGORY_DESC  VARCHAR2 (30),
		GLADESC        VARCHAR2 (60),
		ENTRY_DATE     DATE,
		CATALOG        VARCHAR2 (10)
)
